#define load(n, a) CpuFastSet(a,SPRITE_GFX+(n)*16,16|COPY32)

void
bigload(int dstn, const u8* src, int w, int h)
{
	u32 *d, *s, *p;
	int j, i;
	
	d = (u32 *) (SPRITE_GFX + dstn * 16);
	s = (u32 *) src;
	for(j = 0; j < h; j++){
		p = &d[(j / 8) * (h / 8) * 16 + (j % 8) * 2];
		for(i = 0; i < w; i += 8){
			p[0] = *s++;
			p[1] = *s++;
			p += 16;
		}
	}
}

